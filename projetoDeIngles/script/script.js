document.querySelectorAll('.botao').forEach(botao => {
    botao.addEventListener('click', function(){
        const letra = this.getAttribute('letra');
        const dados = dicionario[letra];

        const palavraIngles = document.querySelector('.palavraIngles');
        const palavraTraduzida = document.querySelector('.palavraTraduzida');
        const frase = document.querySelector('.Frase');

        let image = document.createElement('img');
        let source = document.createElement('source');
        let imgContainer = document.querySelector('.img-container');
        let audioContainer = document.querySelector('.audio-container');
        let audio = document.createElement('audio');

        if (dados) {
            palavraIngles.textContent = dados.ingles.join(", ");
            palavraTraduzida.textContent = dados.traduzido.join(", ");
            frase.textContent = dados.frase;
            image.src = dados.imagem;
            imgContainer.innerHTML = '';
            imgContainer.appendChild(image);
            
            source.setAttribute('src', dados.audio[0]);
            source.setAttribute('type', 'audio/mpeg');
            audio.setAttribute('controls', ''); // Adicionar controles de reprodução
            audio.appendChild(source);
            audioContainer.innerHTML = '';
            audioContainer.appendChild(audio);
            
        



        } else {
            palavraIngles.textContent = "N/A";
            palavraTraduzida.textContent = "N/A";
        }
    });
});